package Sequence;
/**
 * A class that tests the sequence interface.
 * @author Dillon Welsh
 */
public class SequenceDemo
{
   public static void main(String[] args)
   {
	  int n = 1000;
      LastDigitDistribution dist1 = new LastDigitDistribution();
      dist1.process(new SquareSequence(), n);
      System.out.println(dist1.toString());

      LastDigitDistribution dist2 = new LastDigitDistribution();
      dist2.process(new RandomSequence(), n);
      System.out.println(dist2.toString());
      
      LastDigitDistribution dist3 = new LastDigitDistribution();
      dist3.process(new MySequence(), n);
      System.out.println(dist3.toString());
   }
}

